<script>
import M from 'materialize-css'
export default {
    name: "FAQ",
    components: {},
    mounted() {
        var elemsCollapsible = document.querySelectorAll('.collapsible');
        M.Collapsible.init(elemsCollapsible);
    },
    methods: {
        searchFunction() {
            // Declare variables
            let i, txtValue1, txtValue2;
            const input = document.querySelector('#search');
            const filter = input.value.toUpperCase();
            const questionBlock = document.querySelectorAll(".question-block");

            // Loop through all list items, and hide those who don't match the search query
            for (i = 0; i < questionBlock.length; i++) {
                const questionText = questionBlock[i].querySelectorAll(".question .question-txt")[0];
                const answerText = questionBlock[i].querySelectorAll(".answer .answer-txt")[0];
                
                txtValue1 = questionText.textContent || questionText.innerText;
                txtValue2 = answerText.textContent || answerText.innerText;
                
                if ((txtValue1.toUpperCase().indexOf(filter) > -1) || (txtValue2.toUpperCase().indexOf(filter) > -1)) {
                questionBlock[i].style.display = "";
                } else {
                questionBlock[i].style.display = "none";
                }
            }
        }
    }
}
</script>

